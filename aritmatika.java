class aritmatika {
	public static void main(String[] args) {
		System.out.println("Hasil yang Diperoleh");
		int aa = 5;
		int bb = 6;
		double cc = 0.5;
		
		double aabb = aa + bb;
		double aabbcc = aa * bb * cc;
		double bbcc = bb / cc;
		
		System.out.println();
		System.out.println("Nilai aa + bb = " + aabb);
		System.out.println("Nilai aa * bb * cc = " + aabbcc);
		System.out.println("Nilai bb / cc = " + bbcc);
		
		System.out.println();
	}
}